let x = 15;
let y = "15";
// equal
console.log(x == y); 
// not equal
console.log(x != y);
// strictly equal
console.log(x === y);
// strictly not equal
console.log(x !== y);
// greater than
console.log(x > y);
// greater than or equal to
console.log(x < y);
// less than or equal to

console.log(x >= y);

